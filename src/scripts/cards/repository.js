export function getCards() {
  return cards;
}

export function addCard(card, prepend=false) {
  if (prepend) {
    cards.unshift(card)
  } else {
    cards.append(card)
  }
}

export function delCard(card) {
  cards = cards.filter((el) => el.name !== card.name || el.link !== card.link)
}

